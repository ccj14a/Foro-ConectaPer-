    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = campo_nombres.getText().trim();
        String usuario = campo_user.getText().trim();
        String edadTexto = campo_edad.getText().trim();
        char[] passwordArray = campo_contra.getPassword();
        String pass = new String(passwordArray).trim();

// Verificar si algún campo está vacío
        if (nombre.isEmpty() || usuario.isEmpty() || pass.isEmpty() || edadTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
            return;
        }

// Verificar si el usuario ya existe
        if (gestionU.findUser(usuario) != null) {
            JOptionPane.showMessageDialog(null, "Usuario no disponible");
            return;
        }

        int edad;

// Verificar que la edad sea un número válido
        try {
            edad = Integer.parseInt(edadTexto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La edad debe ser un número válido");
            return;
        }

// Crear el nuevo usuario y agregarlo
        Usuario nuevo = new Usuario(nombre, usuario, pass, edad);
        gestionU.addUser(nuevo);

// Mostrar mensaje de éxito y cerrar la ventana actual
        JOptionPane.showMessageDialog(null, "Se ha creado con éxito su cuenta");
        this.dispose();

// Abrir la ventana de login
        Login log = new Login();
        log.setVisible(true);
        log.setLocationRelativeTo(null);


    }//GEN-LAST:event_jButton1ActionPerformed

